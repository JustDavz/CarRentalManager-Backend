package com.carrentalmanager.entity.enums;

/*
 * VehicleCategory è un enum, cioè un tipo che definisce un insieme fisso di valori.
 * Rappresenta le categorie disponibili per i veicoli.
 *
 * Questa categoria viene usata anche per applicare prezzi diversi ai servizi accessori.
 */
public enum VehicleCategory {
    BASIC,    // Veicoli economici
    PREMIUM,  // Veicoli di fascia media
    LUXURY    // Veicoli di fascia alta
}